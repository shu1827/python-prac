# Float
y = 3.14
print("y =", y)

# String
name = "Alice"
print("Hello, " + name)

# Addition
a = 10
b = 5
print("a + b =", a + b)

# Subtraction
print("a - b =", a - b)

# Multiplication
print("a * b =", a * b)

# Division
print("a / b =", a / b)
